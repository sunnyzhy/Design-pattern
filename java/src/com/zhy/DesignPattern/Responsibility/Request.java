package com.zhy.DesignPattern.Responsibility;

public class Request {

	private String name;
	private double days;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getDays() {
		return days;
	}
	public void setDays(double days) {
		this.days = days;
	}
}
